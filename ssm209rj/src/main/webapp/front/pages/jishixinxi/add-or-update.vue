				return new Date().getTime();
			},
			async onSubmitTap() {
				if((!this.ruleForm.jishizhanghao)){
					this.$utils.msg(`技师账号不能为空`);
					return
				}
				if((!this.ruleForm.jishixingming)){
					this.$utils.msg(`技师姓名不能为空`);
					return
				}
				if((!this.ruleForm.nianling)){
					this.$utils.msg(`年龄不能为空`);
					return
				}
				if(this.ruleForm.nianling&&(!this.$validate.isIntNumer(this.ruleForm.nianling))){
