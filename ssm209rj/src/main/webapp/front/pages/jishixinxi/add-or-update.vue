				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='jishizhanghao'){
					this.ruleForm.jishizhanghao = obj[o];
					this.ro.jishizhanghao = true;
					continue;
					}
					if(o=='jishixingming'){
					this.ruleForm.jishixingming = obj[o];
					this.ro.jishixingming = true;
					continue;
					}
					if(o=='nianling'){
					this.ruleForm.nianling = obj[o];
					this.ro.nianling = true;
					continue;
					}
					if(o=='xingbie'){
					this.ruleForm.xingbie = obj[o];
					this.ro.xingbie = true;
					continue;
					}
					if(o=='gongling'){
					this.ruleForm.gongling = obj[o];
					this.ro.gongling = true;
					continue;
